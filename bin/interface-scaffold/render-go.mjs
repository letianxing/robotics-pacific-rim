import { pascalToSnake, toCamelCase, toPascalCase } from "./naming.mjs";

export function renderGoScaffoldFiles(manifest) {
  const files = {
    "internal/api/generated/register.go": renderGoRegister(manifest),
    "internal/service/generated/service.go": renderGoDefaults(manifest),
  };
  for (const iface of manifest.interfaces) {
    if (iface.kind === "topic" && iface.role === "publisher") {
      files[`internal/service/generated/${routeFileName(iface)}_publisher_service.go`] = renderGoPublisherService(manifest, iface);
      continue;
    }
    if (iface.kind === "topic" && iface.role === "subscriber") {
      files[`internal/service/generated/${routeFileName(iface)}_subscriber_service.go`] = renderGoSubscriberService(manifest, iface);
      continue;
    }
    if (iface.kind === "service" && iface.role === "client") {
      files[`internal/service/generated/${routeFileName(iface)}_client_service.go`] = renderGoClientService(manifest, iface);
      continue;
    }
    if (iface.kind !== "service" || iface.role !== "server") {
      continue;
    }
    files[`internal/service/generated/${routeFileName(iface)}_service.go`] = renderGoService(manifest, iface);
  }
  return files;
}

function renderGoRegister(manifest) {
  const protocolImports = goProtocolImports(manifest);
  const registerLines = generatedIdlServices(manifest).map((service) => {
    const alias = protocolAliasForService(manifest, service);
    return `\tif err := ${alias}.RegisterGeneratedInterfaces(ctx, runtime, provider.To${toPascalCase(service)}Provider()); err != nil {
\t\treturn err
\t}`;
  }).join("\n");
  const importBlock = protocolImports ? `${protocolImports}
` : "";
  return `// Code generated by bin/generate-interface-scaffold.mjs; DO NOT EDIT.
// Thin module-local shim. Shared registrar logic lives under pkg/idl.

package generated

import (
\t"context"

\tcommcore "github.com/pacific-rim/pacific-rim/infra/communication/go/core"
\tservicegenerated "${goModuleImport(manifest)}/internal/service/generated"
${importBlock}
)

// RegisterGeneratedInterfaces injects this module's business implementations
// into provider-owned pkg/idl registrars.
func RegisterGeneratedInterfaces(ctx context.Context, runtime *commcore.CommunicationRuntime, providers ...servicegenerated.Provider) error {
\tprovider := servicegenerated.DefaultProvider()
\tif len(providers) > 0 {
\t\tprovider = provider.WithOverrides(providers[0])
\t}
${registerLines || "\t_ = ctx\n\t_ = runtime\n\t_ = provider"}
\treturn nil
}
`;
}

function renderGoDefaults(manifest) {
  const protocolImports = goProtocolImports(manifest);
  const importBlock = protocolImports ? `import (
${protocolImports}
)

` : "";
  const fields = manifest.interfaces.flatMap((iface) => {
    if (iface.kind === "service" && iface.role === "server") {
      return [`\t${providerField(iface)} ${protocolAlias(manifest, iface)}.${goProviderType(iface)}`];
    }
    if (iface.kind === "topic" && iface.role === "publisher") {
      return [`\t${providerField(iface)} ${protocolAlias(manifest, iface)}.${goProviderType(iface)}`];
    }
    if (iface.kind === "topic" && iface.role === "subscriber") {
      return [`\t${providerField(iface)} *${toPascalCase(iface.name)}SubscriberService`];
    }
    return [];
  });
  const defaultFields = manifest.interfaces.flatMap((iface) => {
    if (iface.kind === "service" && iface.role === "server") {
      return [`\t\t${providerField(iface)}: New${toPascalCase(iface.name)}Service(),`];
    }
    if (iface.kind === "topic" && iface.role === "publisher") {
      return [`\t\t${providerField(iface)}: New${toPascalCase(iface.name)}PublisherService(),`];
    }
    if (iface.kind === "topic" && iface.role === "subscriber") {
      return [`\t\t${providerField(iface)}: New${toPascalCase(iface.name)}SubscriberService(),`];
    }
    return [];
  });
  const overrideLines = manifest.interfaces.flatMap((iface) => {
    if (!isModuleLocalBusinessRoute(iface)) {
      return [];
    }
    const fieldName = providerField(iface);
    return [
      `\tif overrides.${fieldName} != nil {`,
      `\t\tbase.${fieldName} = overrides.${fieldName}`,
      `\t}`,
    ];
  });
  return `// Code generated by bin/generate-interface-scaffold.mjs; DO NOT EDIT.
// Provider slots are the only supported injection points for business logic.

package generated

${importBlock}// Provider contains business implementations for routes this service exposes.

// Leave a field nil to use the generated no-op/default implementation.
type Provider struct {
${fields.length > 0 ? fields.join("\n") : "\t// No provider routes are configured for this service."}
}

func DefaultProvider() Provider {
\treturn Provider{
${defaultFields.join("\n")}
\t}
}

func (base Provider) WithOverrides(overrides Provider) Provider {
${overrideLines.length > 0 ? overrideLines.join("\n") : "\t_ = overrides"}
\treturn base
}

${renderGoProviderAdapters(manifest)}
`;
}

function renderGoService(manifest, iface) {
  const typeName = `${toPascalCase(iface.name)}Service`;
  const alias = protocolAlias(manifest, iface);
  return `// Scaffold generated by bin/generate-interface-scaffold.mjs.
// User-editable service implementation. The generator creates this file when missing;
// do not pass --force after adding business logic unless you intentionally reset it.

package generated

import (
\t"context"

\t${alias} "github.com/pacific-rim/pacific-rim/pkg/idl/${manifest.idlService || manifest.module}/generated/go"
)

var _ ${alias}.${goProviderType(iface)} = (*${typeName})(nil)

type ${typeName} struct{}

func New${typeName}() *${typeName} {
\treturn &${typeName}{}
}

func (s *${typeName}) Execute(ctx context.Context, payload []byte) ([]byte, error) {
\t_ = ctx
\t_ = payload
\treturn nil, nil
}
`;
}

function renderGoPublisherService(manifest, iface) {
  const typeName = `${toPascalCase(iface.name)}PublisherService`;
  const alias = protocolAlias(manifest, iface);
  return `// Scaffold generated by bin/generate-interface-scaffold.mjs.
// User-editable publish implementation. The generator creates this file when missing;
// do not pass --force after adding business logic unless you intentionally reset it.

package generated

import (
\t"context"

\tcommcore "github.com/pacific-rim/pacific-rim/infra/communication/go/core"
\t${alias} "github.com/pacific-rim/pacific-rim/pkg/idl/${manifest.idlService || manifest.module}/generated/go"
)

var _ ${alias}.${goProviderType(iface)} = (*${typeName})(nil)

type ${typeName} struct{}

func New${typeName}() *${typeName} {
\treturn &${typeName}{}
}

func (s *${typeName}) Execute(ctx context.Context, endpoint commcore.BoundEndpoint, payload []byte) error {
\treturn endpoint.Bus.Publish(ctx, endpoint.Channel, payload)
}
`;
}

function renderGoSubscriberService(_manifest, iface) {
  const typeName = `${toPascalCase(iface.name)}SubscriberService`;
  return `// Scaffold generated by bin/generate-interface-scaffold.mjs.
// User-editable subscriber implementation. The generator creates this file when missing;
// do not pass --force after adding business logic unless you intentionally reset it.

package generated

import "context"

type ${typeName} struct{}

func New${typeName}() *${typeName} {
\treturn &${typeName}{}
}

func (s *${typeName}) Receive(ctx context.Context, payload []byte) error {
\t_ = ctx
\t_ = payload
\treturn nil
}
`;
}

function renderGoClientService(manifest, iface) {
  const typeName = `${toPascalCase(iface.name)}ClientService`;
  const alias = protocolAliasForService(manifest, manifest.idlService || manifest.module);
  const defaultRoute = expandedRouteNames(iface)[0] ?? iface.name;
  return `// Scaffold generated by bin/generate-interface-scaffold.mjs.
// User-editable downstream service client helper. The generator creates this file when missing;
// do not pass --force after adding business logic unless you intentionally reset it.

package generated

import (
\t"context"
\t"time"

\tcommcore "github.com/pacific-rim/pacific-rim/infra/communication/go/core"
\t${alias} "github.com/pacific-rim/pacific-rim/pkg/idl/${manifest.idlService || manifest.module}/generated/go"
)

type ${typeName} struct {
\tclient *${alias}.${toPascalCase(iface.name)}Client
\tDefaultRoute string
}

func New${typeName}() *${typeName} {
\treturn &${typeName}{
\t\tclient: ${alias}.New${toPascalCase(iface.name)}Client(nil),
\t\tDefaultRoute: "${defaultRoute}",
\t}
}

func (s *${typeName}) Request(ctx context.Context, runtime *commcore.CommunicationRuntime, payload []byte, timeout time.Duration, routeName string) ([]byte, error) {
\tif routeName == "" {
\t\trouteName = s.DefaultRoute
\t}
\treturn s.client.Request(ctx, runtime, payload, timeout, routeName)
}
`;
}

function providerField(iface) {
  let suffix = "Service";
  if (iface.kind === "topic") {
    suffix = iface.role === "subscriber" ? "Subscriber" : "Publisher";
  } else if (iface.role === "client") {
    suffix = "Client";
  }
  return `${toPascalCase(iface.name)}${suffix}`;
}

function goProviderType(iface) {
  return iface.kind === "topic" ? `${toPascalCase(iface.name)}PublisherService` : `${toPascalCase(iface.name)}Service`;
}

function routeFileName(iface) {
  return pascalToSnake(iface.name) || "route";
}

function goModuleImport(manifest) {
  if (manifest.goModulePath) {
    return manifest.goModulePath;
  }
  return `github.com/pacific-rim/pacific-rim/module/service/${manifest.module}`;
}

function goProtocolImports(manifest) {
  const services = generatedIdlServices(manifest);
  if (services.length === 0) {
    return "";
  }
  return services
    .map((service, index) => {
      const alias = services.length === 1 ? "idlprotocol" : `idlprotocol${index + 1}`;
      return `\t${alias} "github.com/pacific-rim/pacific-rim/pkg/idl/${service}/generated/go"`;
    })
    .join("\n");
}

function providerIdlServices(manifest) {
  return Array.from(new Set((manifest.interfaces ?? [])
    .filter(isProviderRoute)
    .map((item) => item.idlService || manifest.idlService))).sort();
}

function generatedIdlServices(manifest) {
  if (!Array.isArray(manifest.interfaces) || manifest.interfaces.length === 0) {
    return [];
  }
  const service = String(manifest.idlService || manifest.module || "").trim();
  return service ? [service] : [];
}

function protocolAlias(manifest, iface) {
  return protocolAliasForService(manifest, manifest.idlService || manifest.module);
}

function protocolAliasForService(manifest, service) {
  const services = generatedIdlServices(manifest);
  if (services.length <= 1) {
    return "idlprotocol";
  }
  const index = services.indexOf(service);
  return `idlprotocol${index + 1}`;
}

function isProviderRoute(iface) {
  return (iface.kind === "service" && iface.role === "server") || (iface.kind === "topic" && iface.role === "publisher");
}

function isModuleLocalBusinessRoute(iface) {
  return isProviderRoute(iface) || (iface.kind === "topic" && iface.role === "subscriber");
}

function renderGoProviderAdapters(manifest) {
  return generatedIdlServices(manifest).map((service) => {
    const alias = protocolAliasForService(manifest, service);
    const fields = (manifest.interfaces ?? [])
      .filter(isModuleLocalBusinessRoute)
      .map((iface) => `\t\t${providerField(iface)}: provider.${providerField(iface)},`)
      .join("\n");
    return `func (provider Provider) To${toPascalCase(service)}Provider() ${alias}.Provider {
\treturn ${alias}.Provider{
${fields}
\t}
}`;
  }).join("\n\n");
}

function expandedRouteNames(iface) {
  if (Array.isArray(iface.routeNames) && iface.routeNames.length > 0) {
    return iface.routeNames;
  }
  if (!Array.isArray(iface.bindings) || iface.bindings.length === 0) {
    return [iface.name];
  }
  return iface.bindings.map((binding, index) => `${iface.name}_${canonicalRouteName(bindingName(binding, index))}`);
}

function bindingName(binding, index) {
  const values = [
    binding.name,
    binding.middleware,
    binding.transport,
    binding.standard,
    binding.service,
    binding.request,
    binding.request_channel,
    binding.response,
    binding.response_channel,
    binding.topic,
    binding.dds_topic,
    binding.subject,
    binding.nats_subject,
    binding.address,
  ].map((value) => String(value || "").trim()).filter(Boolean);
  return values.length > 0 ? values.join("_") : `binding_${index}`;
}

function canonicalRouteName(value) {
  return String(value)
    .replace(/[^A-Za-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "");
}
