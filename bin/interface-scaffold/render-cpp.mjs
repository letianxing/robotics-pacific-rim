import { indentLines, namespacePart, pascalToSnake, toCamelCase, toPascalCase } from "./naming.mjs";

export function renderCppScaffoldFiles(manifest) {
  const files = {};
  for (const iface of manifest.interfaces) {
    if (iface.kind === "topic" && iface.role === "publisher") {
      files[cppApiPublisherPath(manifest, iface)] = renderTopicPublisher(manifest, iface);
      files[cppPublisherServicePath(manifest, iface)] = renderTopicPublisherService(manifest, iface);
      continue;
    }
    if (iface.kind === "topic" && iface.role === "subscriber") {
      files[cppSubscriberServicePath(manifest, iface)] = renderTopicSubscriberService(manifest, iface);
      continue;
    }
    if (iface.kind === "service" && iface.role === "client") {
      files[cppClientServicePath(manifest, iface)] = renderClientService(manifest, iface);
      continue;
    }
    if (iface.kind !== "service" || iface.role !== "server") {
      continue;
    }
    files[cppApiHandlerPath(manifest, iface)] = renderApiHandler(manifest, iface);
    files[cppServicePath(manifest, iface)] = renderService(manifest, iface);
  }
  if (manifest.includeRuntimeRegistry) {
    files[cppRegistryPath(manifest)] = renderCppRegistry(manifest);
  }
  return files;
}

function renderTopicPublisher(manifest, iface) {
  const className = `${toPascalCase(iface.name)}ApiPublisher`;
  const namespaceName = cppNamespace(manifest);
  const protocolNamespace = `pacific_rim::idl::${cppIdlNamespace(manifest.idlService || manifest.module)}::generated`;
  return `// Code generated by bin/generate-interface-scaffold.mjs; DO NOT EDIT.
// Thin module-local publisher shim. Shared route wiring lives under pkg/idl.
#pragma once

#include <cstdint>
#include <memory>
#include <string>
#include <vector>

#include "infra/communication/cpp/core/bootstrap.hpp"
#include "pkg/idl/${manifest.idlService || manifest.module}/generated/cpp/publisher.hpp"
#include "service/generated/include/${routeFileName(iface)}_publisher_service.hpp"

namespace pacific_rim::generated::${namespaceName}::api::publisher {

class ${className} final : public ${protocolNamespace}::BytePublisher {
 public:
  void Publish(
      pacific_rim::communication::core::CommunicationRuntime& runtime,
      const std::vector<std::uint8_t>& payload) override {
    auto service = std::make_shared<pacific_rim::generated::${namespaceName}::service::${toPascalCase(iface.name)}PublisherService>();
    ${protocolNamespace}::${toPascalCase(iface.name)}Publisher publisher(service);
    publisher.Publish(runtime, payload);
  }
};

}  // namespace pacific_rim::generated::${namespaceName}::api::publisher
`;
}

