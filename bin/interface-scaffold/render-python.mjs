function isProviderRoute(iface) {
  return (iface.kind === "service" && iface.role === "server") || (iface.kind === "topic" && iface.role === "publisher");
}

function isModuleLocalBusinessRoute(iface) {
  return isProviderRoute(iface) || (iface.kind === "topic" && iface.role === "subscriber");
}

function providerIdlServices(manifest) {
  return Array.from(new Set((manifest.interfaces ?? [])
    .filter(isProviderRoute)
    .map((item) => item.idlService || manifest.idlService))).sort();
}

function generatedIdlServices(manifest) {
  if (!Array.isArray(manifest.interfaces) || manifest.interfaces.length === 0) {
    return [];
  }
  const service = String(manifest.idlService || manifest.module || "").trim();
  return service ? [service] : [];
}

function providerAttr(iface) {
  const name = routeFileName(iface);
  if (iface.kind === "topic") {
    return iface.role === "subscriber" ? `${name}_subscriber` : `${name}_publisher`;
  }
  return iface.role === "client" ? `${name}_client` : `${name}_service`;
}

function pythonBase(manifest) {
  return String(manifest.runtimePackage ?? manifest.module ?? "").replace(/[^A-Za-z0-9_]/g, "_");
}

function routeFileName(iface) {
  return pascalToSnake(iface.name) || "route";
}

function roleLoaderName(service, role) {
  return `${service.replace(/[^A-Za-z0-9]+/g, "_")}_${role}`;
}

function renderPythonProviderAdapters(manifest) {
  return generatedIdlServices(manifest).map((service) => {
    const methodName = `to_${service.replace(/[^A-Za-z0-9]+/g, "_")}_provider`;
    const fields = (manifest.interfaces ?? [])
      .filter(isModuleLocalBusinessRoute)
      .map((iface) => `      ${providerAttr(iface)}=self.${providerAttr(iface)},`)
      .join("\n");
    return `def ${methodName}(self, protocol_module):
  return protocol_module.Provider(
${fields}
  )


Provider.${methodName} = ${methodName}`;
  }).join("\n\n");
}

function expandedRouteNames(iface) {
  if (Array.isArray(iface.routeNames) && iface.routeNames.length > 0) {
    return iface.routeNames;
  }
  if (!Array.isArray(iface.bindings) || iface.bindings.length === 0) {
    return [iface.name];
  }
  return iface.bindings.map((binding, index) => `${iface.name}_${canonicalRouteName(bindingName(binding, index))}`);
}

function bindingName(binding, index) {
  const values = [
    binding.name,
    binding.middleware,
    binding.transport,
    binding.standard,
    binding.service,
    binding.request,
    binding.request_channel,
    binding.response,
    binding.response_channel,
    binding.topic,
    binding.dds_topic,
    binding.subject,
    binding.nats_subject,
    binding.address,
  ].map((value) => String(value || "").trim()).filter(Boolean);
  return values.length > 0 ? values.join("_") : `binding_${index}`;
}

function canonicalRouteName(value) {
  return String(value)
    .replace(/[^A-Za-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "");
}
