import { pascalToSnake, toPascalCase } from "./naming.mjs";

export function renderPythonScaffoldFiles(manifest) {
  const base = pythonBase(manifest);
  const files = {
    [`${base}/api/generated/__init__.py`]: "# Code generated by bin/generate-interface-scaffold.mjs; DO NOT EDIT.\n",
    [`${base}/api/generated/register.py`]: renderPythonRegister(manifest),
    [`${base}/service/generated/__init__.py`]: "# Code generated by bin/generate-interface-scaffold.mjs; DO NOT EDIT.\n",
    [`${base}/service/generated/defaults.py`]: renderPythonDefaults(manifest),
  };
  for (const iface of manifest.interfaces) {
    if (iface.kind === "topic" && iface.role === "publisher") {
      files[`${base}/service/generated/${routeFileName(iface)}_publisher_service.py`] = renderPythonPublisherService(manifest, iface);
      continue;
    }
    if (iface.kind === "topic" && iface.role === "subscriber") {
      files[`${base}/service/generated/${routeFileName(iface)}_subscriber_service.py`] = renderPythonSubscriberService(manifest, iface);
      continue;
    }
    if (iface.kind === "service" && iface.role === "client") {
      files[`${base}/service/generated/${routeFileName(iface)}_client_service.py`] = renderPythonClientService(manifest, iface);
      continue;
    }
    if (iface.kind !== "service" || iface.role !== "server") {
      continue;
    }
    files[`${base}/service/generated/${routeFileName(iface)}_service.py`] = renderPythonService(manifest, iface);
  }
  return files;
}

function renderPythonRegister(manifest) {
  const services = generatedIdlServices(manifest);
  const providerLines = services.map((service) => {
    const loaderName = roleLoaderName(service, "registry");
    const providerLoaderName = roleLoaderName(service, "provider");
    const methodName = `to_${service.replace(/[^A-Za-z0-9]+/g, "_")}_provider`;
    return `  ${loaderName} = _load_role("${service}", "registry")
  ${providerLoaderName} = _load_role("${service}", "provider")
  await ${loaderName}.register_generated_interfaces(runtime, provider.${methodName}(${providerLoaderName}))`;
  }).join("\n");
  const lines = providerLines;
  return `# Code generated by bin/generate-interface-scaffold.mjs; DO NOT EDIT.
# Thin module-local shim. Shared registrar logic lives under pkg/idl.

from __future__ import annotations

import importlib.util
from pathlib import Path

from ${manifest.runtimePackage}.service.generated.defaults import Provider, default_provider


def _load_role(idl_service: str, role: str):
  for root in Path(__file__).resolve().parents:
    candidate = root / "pkg" / "idl" / idl_service / "generated" / "python" / f"{role}.py"
    if candidate.exists():
      spec = importlib.util.spec_from_file_location(f"{idl_service}_generated_{role}", candidate)
      module = importlib.util.module_from_spec(spec)
      assert spec and spec.loader
      spec.loader.exec_module(module)
      return module
  raise ImportError(f"missing pkg/idl/{idl_service}/generated/python/{role}.py")


async def register_generated_interfaces(runtime, provider: Provider | None = None) -> None:
  """Inject this module's business implementations into pkg/idl registrars."""
  provider = provider or default_provider()
${lines || "  _ = runtime\n  _ = provider"}
`;
}

function renderPythonDefaults(manifest) {
  const imports = manifest.interfaces.flatMap((iface) => {
    if (iface.kind === "service" && iface.role === "server") {
      return [`from .${routeFileName(iface)}_service import ${toPascalCase(iface.name)}Service`];
    }
    if (iface.kind === "topic" && iface.role === "publisher") {
      return [`from .${routeFileName(iface)}_publisher_service import ${toPascalCase(iface.name)}PublisherService`];
    }
    if (iface.kind === "topic" && iface.role === "subscriber") {
      return [`from .${routeFileName(iface)}_subscriber_service import ${toPascalCase(iface.name)}SubscriberService`];
    }
    return [];
  });
  const params = manifest.interfaces.flatMap((iface) => {
    if (!isModuleLocalBusinessRoute(iface)) {
      return [];
    }
    return [`    ${providerAttr(iface)}=None,`];
  });
  const assigns = manifest.interfaces.flatMap((iface) => {
    if (iface.kind === "service" && iface.role === "server") {
      return [`    self.${providerAttr(iface)} = ${providerAttr(iface)} or ${toPascalCase(iface.name)}Service()`];
    }
    if (iface.kind === "topic" && iface.role === "publisher") {
      return [`    self.${providerAttr(iface)} = ${providerAttr(iface)} or ${toPascalCase(iface.name)}PublisherService()`];
    }
    if (iface.kind === "topic" && iface.role === "subscriber") {
      return [`    self.${providerAttr(iface)} = ${providerAttr(iface)} or ${toPascalCase(iface.name)}SubscriberService()`];
    }
    return [];
  });
  const initHeader = params.length > 0
    ? `  def __init__(
    self,
${params.join("\n")}
  ):`
    : "  def __init__(self):";
  return `# Code generated by bin/generate-interface-scaffold.mjs; DO NOT EDIT.
# Provider slots are the supported injection points for business logic.

from __future__ import annotations

${imports.join("\n")}


class Provider:
${initHeader}
${assigns.length > 0 ? assigns.join("\n") : "    pass"}


def default_provider() -> Provider:
  return Provider()


${renderPythonProviderAdapters(manifest)}
`;
}

function renderPythonService(manifest, iface) {
  const className = `${toPascalCase(iface.name)}Service`;
  const portName = className;
  return `# Scaffold generated by bin/generate-interface-scaffold.mjs.
# User-editable service implementation. Do not pass --force after adding business logic
# unless you intentionally reset this file.

from __future__ import annotations

${pythonProtocolLoader(manifest.idlService || manifest.module, portName)}


class ${className}:
  async def execute(self, payload: bytes) -> bytes:
    _ = payload
    return b""


_protocol_check: ${portName} = ${className}()
`;
}

function renderPythonPublisherService(manifest, iface) {
  const className = `${toPascalCase(iface.name)}PublisherService`;
  const portName = className;
  return `# Scaffold generated by bin/generate-interface-scaffold.mjs.
# User-editable publish implementation. Do not pass --force after adding business logic
# unless you intentionally reset this file.

from __future__ import annotations

${pythonPublisherProtocolLoader(manifest.idlService || manifest.module, portName)}


class ${className}:
  async def execute(self, endpoint, payload: bytes) -> None:
    await endpoint.bus.publish_bytes(endpoint.channel, payload)


_protocol_check: ${portName} = ${className}()
`;
}

function renderPythonSubscriberService(manifest, iface) {
  const className = `${toPascalCase(iface.name)}SubscriberService`;
  return `# Scaffold generated by bin/generate-interface-scaffold.mjs.
# User-editable subscriber implementation. Do not pass --force after adding business logic
# unless you intentionally reset this file.

from __future__ import annotations

${pythonRoleLoader(manifest.idlService || manifest.module, "subscriber", `${toPascalCase(iface.name)}Receiver`)}


class ${className}(${toPascalCase(iface.name)}Receiver):
  async def receive(self, payload: bytes) -> None:
    _ = payload

`;
}

function renderPythonClientService(manifest, iface) {
  const className = `${toPascalCase(iface.name)}ClientService`;
  const defaultRoute = expandedRouteNames(iface)[0] ?? iface.name;
  return `# Scaffold generated by bin/generate-interface-scaffold.mjs.
# User-editable downstream service client helper. Do not pass --force after adding business logic
# unless you intentionally reset this file.

from __future__ import annotations

${pythonRoleLoader(manifest.idlService || manifest.module, "client", `${toPascalCase(iface.name)}Client`)}

class ${className}:
  def __init__(self, client=None):
    self.client = client or ${toPascalCase(iface.name)}Client()
    self.default_route = "${defaultRoute}"

  async def request(
    self,
    runtime,
    payload: bytes,
    timeout_sec: float = 2.0,
    route_name: str | None = None,
  ) -> bytes:
    return await self.client.request(runtime, payload, timeout_sec=timeout_sec, route_name=route_name or self.default_route)

`;
}

function pythonRoleLoader(idlService, role, symbolName) {
  return `import importlib.util
from pathlib import Path

_ROLE_PATH = None
for _ROOT in Path(__file__).resolve().parents:
  _CANDIDATE = _ROOT / "pkg" / "idl" / "${idlService}" / "generated" / "python" / "${role}.py"
  if _CANDIDATE.exists():
    _ROLE_PATH = _CANDIDATE
    break
if _ROLE_PATH is None:
  raise ImportError("missing pkg/idl/${idlService}/generated/python/${role}.py")
_ROLE_SPEC = importlib.util.spec_from_file_location("${idlService}_generated_${role}", _ROLE_PATH)
_ROLE_MODULE = importlib.util.module_from_spec(_ROLE_SPEC)
assert _ROLE_SPEC and _ROLE_SPEC.loader
_ROLE_SPEC.loader.exec_module(_ROLE_MODULE)
${symbolName} = _ROLE_MODULE.${symbolName}`;
}

