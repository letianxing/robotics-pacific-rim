#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
MODULE_DIR="$(cd "${SCRIPT_DIR}/.." && pwd)"
ROOT_DIR="$(cd "${MODULE_DIR}/../../../.." && pwd)"
DEST_DIR="${MODULE_DIR}/internal/ros2msgs"
SERVICE_NAME="$(basename "${MODULE_DIR}")"
MANIFEST_PATH="${ROOT_DIR}/pkg/idl/${SERVICE_NAME}/protocol_manifest.json"
ROS_DISTRO="${ROS_DISTRO:-{{rosDistro}}}"
ROS_PREFIX="${PACIFIC_RIM_GO_ROS_PREFIX:-/opt/ros/${ROS_DISTRO}}"

if [[ -f "${ROS_PREFIX}/setup.bash" ]]; then
  # shellcheck disable=SC1090
  set +u
  source "${ROS_PREFIX}/setup.bash"
  set -u
fi

if ! command -v go >/dev/null 2>&1; then
  echo "go executable is required to generate ROS2 Go bindings" >&2
  exit 1
fi

module_path="$(go -C "${MODULE_DIR}" list -m)"
rm -rf "${DEST_DIR}"
mkdir -p "${DEST_DIR}"
cat > "${DEST_DIR}/.gitignore" <<'EOF'
*
!.gitignore
EOF

mapfile -t ros_packages < <(python3 - "${MANIFEST_PATH}" <<'PY'
import json
import os
import sys

manifest_path = sys.argv[1]
packages = set()

def add_ros_type(value):
    if not isinstance(value, str):
        return
    parts = [part for part in value.strip("/").split("/") if part]
    if len(parts) >= 3 and parts[1] in {"msg", "srv", "action"}:
        packages.add(parts[0])

if os.path.exists(manifest_path):
    with open(manifest_path, "r", encoding="utf-8") as handle:
        manifest = json.load(handle)
    for iface in manifest.get("interfaces", []):
        bindings = iface.get("bindings", [])
        uses_ros2 = any(str(binding.get("transport", "")).replace("-", "_").startswith("ros2") for binding in bindings)
        if not uses_ros2:
            continue
        for protocol in iface.get("protocols", []):
            if protocol.get("format") in {"ros2_msg", "ros2_srv", "ros2_action"}:
                add_ros_type(protocol.get("type"))
        adapter_values = []
        payload = iface.get("payload") or {}
        contract = iface.get("contract") or {}
        if str(payload.get("format", "")).lower() == "protobuf":
            adapter_values.extend(binding.get("adapter", "") for binding in bindings)
        if str(contract.get("format", "")).lower() == "protobuf_rpc":
            adapter_values.extend(binding.get("adapter", "") for binding in bindings)
        for binding in bindings:
            metadata = binding.get("metadata") or {}
            adapter = str(binding.get("adapter", "") or metadata.get("adapter", "")).replace("-", "_")
            if adapter == "ros2_proto_envelope":
                packages.add("common")
            add_ros_type(binding.get("message_type"))
            add_ros_type(binding.get("service_type"))
        if any(str(value).replace("-", "_") == "ros2_proto_envelope" for value in adapter_values):
            packages.add("common")

for package in sorted(packages):
    print(package)
PY
)

if [[ ${#ros_packages[@]} -eq 0 ]]; then
  cat > "${DEST_DIR}/msgs.gen.go" <<'EOF'
//go:build pacific_rim_ros2_rclgo

// Code generated by tools/generate-ros2-bindings.sh; DO NOT EDIT.

package msgs
EOF
  exit 0
fi

include_args=()
for package in "${ros_packages[@]}"; do
  include_args+=(--include-package-deps "${package}")
done

go -C "${MODULE_DIR}" run github.com/tiiuae/rclgo/cmd/rclgo-gen generate \
  --dest-path "${DEST_DIR}" \
  --message-module-prefix "${module_path}/internal/ros2msgs" \
  --cgo-flags-path "${DEST_DIR}/cgo-flags.env" \
  "${include_args[@]}"

for package in "${ros_packages[@]}"; do
  if [[ ! -d "${DEST_DIR}/${package}" ]]; then
    echo "rclgo-gen did not generate ROS2 package ${package}; ensure the package is built and visible in AMENT_PREFIX_PATH" >&2
    exit 1
  fi
done

while IFS= read -r -d '' file; do
  if head -n 1 "${file}" | grep -qx '//go:build pacific_rim_ros2_rclgo'; then
    continue
  fi
  tmp="${file}.tmp"
  {
    echo "//go:build pacific_rim_ros2_rclgo"
    echo
    cat "${file}"
  } > "${tmp}"
  mv "${tmp}" "${file}"
done < <(find "${DEST_DIR}" -name '*.go' -print0)
